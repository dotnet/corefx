            get { return new XmlCharType(s_CharProperties); }
        }

        [MethodImpl(MethodImplOptions.AggressiveInlining)]
        public bool IsWhiteSpace(char ch)
        {
            return (_charProperties[ch >> 8][ch & 0xff] & fWhitespace) != 0;
        }

